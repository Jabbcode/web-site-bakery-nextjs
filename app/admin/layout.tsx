export default function AdminLayout({ children }: AdminLayoutProps) {
  const { isSignedIn, isLoaded } = useAuth()
  const { user } = useUser()
  const router = useRouter()
  const pathname = usePathname()

  // Check if user is admin
  const isAdmin = user?.publicMetadata?.role === UserRole.ADMIN

  // Redirect if not signed in or not admin
  if (isLoaded && (!isSignedIn || !isAdmin)) {
    router.push('/en')
    return null
  }

  if (!isLoaded) {
    return (
      <div className="min-h-screen bg-cream flex items-center justify-center">
        <div className="inline-block animate-spin rounded-full h-12 w-12 border-b-2 border-primary"></div>
      </div>
    )
  }

  return (
    <div className="min-h-screen bg-gray-100">
      {/* Top Header */}
      <header className="bg-white border-b border-gray-200 sticky top-0 z-10">
        <div className="px-4 sm:px-6 lg:px-8">
          <div className="flex h-16 items-center justify-between">
            <div className="flex items-center">
              <h1 className="text-2xl font-display text-dark">SwissDelight Admin</h1>
            </div>
            <div className="flex items-center space-x-4">
              <Link
                href="/en"
                className="text-sm text-text hover:text-dark transition-colors"
              >
                View Store
              </Link>
              <div className="text-sm text-text">
                {user?.firstName} {user?.lastName}
              </div>
            </div>
          </div>
        </div>
      </header>

      <div className="flex">
        {/* Sidebar */}
        <aside className="w-64 bg-white border-r border-gray-200 min-h-[calc(100vh-4rem)] sticky top-16">
          <nav className="p-4 space-y-1">
            {ADMIN_NAVIGATION.map((item) => {
              const isActive = pathname === item.href

              return (
                <Link
                  key={item.href}
                  href={item.href}
                  className={`
                    flex items-center space-x-3 px-4 py-3 rounded-md transition-colors
                    ${
                      isActive
                        ? 'bg-primary text-white'
                        : 'text-text hover:bg-gray-100 hover:text-dark'
                    }
                  `}
                >
                  <span className={isActive ? 'text-white' : 'text-text'}>{item.icon}</span>
                  <span className="font-medium">{item.name}</span>
                </Link>
              )
            })}
          </nav>
        </aside>

        {/* Main Content */}
        <main className="flex-1 p-8">{children}</main>
      </div>
    </div>
  )
}
